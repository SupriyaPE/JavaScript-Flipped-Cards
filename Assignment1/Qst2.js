// 1. String → "Hello"


// 2. Number → 10, 10.5


// 3. Boolean → true, false


// 4. Null → intentional empty value


// 5. Undefined → variable declared but not assigned


// 6. Symbol → unique identifiers


// 7. BigInt → large integers: 123n


